    out << "  24x24/  32x32/  48x48/  64x64/            # Additional PNG sizes\n";
    out << "  128x128/  256x256/                         # High-res PNG sizes\n";
    out << "  gresource.xml                              # GLib resource bundle\n";
    out << "  index.theme                                # Freedesktop theme descriptor\n";
    out << "  README.md                                  # This file\n";
