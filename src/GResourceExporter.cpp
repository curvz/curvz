// ── Main export ───────────────────────────────────────────────────────────────

ExportResult export_theme(
        const std::vector<ExportEntry>& entries,
        const std::string& output_dir,
        const std::string& theme_name,
        const std::string& theme_comment,
        std::function<void(int,int,const std::string&)> progress_cb)
{
    ExportResult result;
    result.output_dir = output_dir;

    // Count exportable icons
    std::vector<const ExportEntry*> valid;
    for (const auto& e : entries) {
        if (!e.include || !e.doc) continue;
        if (e.doc->export_name.empty() || e.doc->export_category.empty()) {
            result.icons_skipped++;
            continue;
        }
        valid.push_back(&e);
    }

    if (valid.empty()) {
        result.error_message = "No icons to export. Assign a name and category to each icon in the inspector.";
        return result;
    }

    if (!ensure_dir(output_dir)) {
        result.error_message = "Cannot create output directory: " + output_dir;
        return result;
    }

    // Collect categories used
    std::set<std::string> categories;
    for (const auto* ep : valid)
        categories.insert(ep->doc->export_category);

    // Create all output subdirectories
    static constexpr int k_sizes[] = { 16, 24, 32, 48, 64, 128, 256 };
    for (const auto& cat : categories) {
        if (!ensure_dir(output_dir + "/scalable/" + cat)) {
            result.error_message = "Cannot create scalable/" + cat;
            return result;
        }
        for (int sz : k_sizes) {
            std::string s = std::to_string(sz);
            if (!ensure_dir(output_dir + "/" + s + "x" + s + "/" + cat)) {
                result.error_message = "Cannot create " + s + "x" + s + "/" + cat;
                return result;
            }
        }
    }

    // Export each icon
    const int total = (int)valid.size();
    for (int i = 0; i < total; ++i) {
        const CurvzDocument& doc = *valid[i]->doc;
        const std::string& name  = doc.export_name;
        const std::string& cat   = doc.export_category;
        // Strip trailing -symbolic if user already included it — we always append it.
        std::string base = name;
        if (base.size() > 9 && base.substr(base.size() - 9) == "-symbolic")
            base = base.substr(0, base.size() - 9);
        const std::string stem   = base + "-symbolic";

        if (progress_cb) progress_cb(i, total, name);

        // SVG
        std::string svg_path = output_dir + "/scalable/" + cat + "/" + stem + ".svg";
        std::string svg = optimise_svg(doc);
        if (!write_text(svg_path, svg)) {
            LOG_ERROR("GResourceExporter: failed to write SVG for '{}'", name);
            continue;
        }

        // PNGs
        bool any_failed = false;
        for (int sz : k_sizes) {
            std::string s   = std::to_string(sz);
            std::string png = output_dir + "/" + s + "x" + s + "/" + cat + "/" + stem + ".png";
            if (!export_png(doc, png, sz)) {
                LOG_ERROR("GResourceExporter: failed to write {}px PNG for '{}'", sz, name);
                any_failed = true;
            }
        }
        if (!any_failed)
            result.icons_written++;
        else
            result.icons_skipped++;
    }

    if (progress_cb) progress_cb(total, total, "");

    // Determine resource prefix from theme name (lowercase, no spaces)
    std::string prefix = "/" + theme_name;
    for (char& c : prefix) {
        if (c == ' ') c = '-';
        c = (char)std::tolower((unsigned char)c);
    }

    // gresource.xml
    std::string gres = build_gresource_xml(entries, prefix);
    write_text(output_dir + "/gresource.xml", gres);

    // index.theme
    std::string theme = build_index_theme(theme_name, theme_comment, categories);
    write_text(output_dir + "/index.theme", theme);

    // README.md
    std::string readme = build_readme(
        theme_name,
        fs::path(output_dir).filename().string(),
        prefix);
    write_text(output_dir + "/README.md", readme);

    LOG_INFO("GResourceExporter: exported {} icons to '{}'",
             result.icons_written, output_dir);

    result.success = (result.icons_written > 0);
    if (!result.success && result.error_message.empty())
        result.error_message = "All icons failed to write. Check log for details.";

    return result;
}

