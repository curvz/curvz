// ── Directory helpers ─────────────────────────────────────────────────────────

static bool ensure_dir(const std::string& path) {
    std::error_code ec;
    fs::create_directories(path, ec);
    if (ec) {
        LOG_ERROR("GResourceExporter: cannot create dir '{}': {}", path, ec.message());
        return false;
    }
    return true;
}

static bool write_text(const std::string& path, const std::string& content) {
    std::ofstream f(path);
    if (!f.is_open()) {
        LOG_ERROR("GResourceExporter: cannot write '{}'", path);
        return false;
    }
    f << content;
    return true;
}

// ── gresource.xml ─────────────────────────────────────────────────────────────
// References scalable/ (SVG) and 16x16/ (PNG fallback) per GTK convention.

static std::string build_gresource_xml(
        const std::vector<ExportEntry>& entries,
        const std::string& prefix)
{
    // Collect categories and icons
    // category → list of icon names
    std::map<std::string, std::vector<std::string>> by_cat;
    for (const auto& e : entries) {
        if (!e.include || !e.doc) continue;
        if (e.doc->export_name.empty() || e.doc->export_category.empty()) continue;
        std::string base = e.doc->export_name;
        if (base.size() > 9 && base.substr(base.size() - 9) == "-symbolic")
            base = base.substr(0, base.size() - 9);
        by_cat[e.doc->export_category].push_back(base);
    }

    std::ostringstream out;
    out << "<?xml version=\"1.0\" encoding=\"UTF-8\"?>\n";
    out << "<gresources>\n";

    // scalable/<category>/ entries
    for (const auto& [cat, names] : by_cat) {
        out << "  <gresource prefix=\"" << prefix << "/icons/scalable/" << cat << "\">\n";
        for (const auto& name : names)
            out << "    <file preprocess=\"xml-stripblanks\">" << name << "-symbolic.svg</file>\n";
        out << "  </gresource>\n";
    }

    // 16x16/<category>/ entries
    for (const auto& [cat, names] : by_cat) {
        out << "  <gresource prefix=\"" << prefix << "/icons/16x16/" << cat << "\">\n";
        for (const auto& name : names)
            out << "    <file>" << name << "-symbolic.png</file>\n";
        out << "  </gresource>\n";
    }

    out << "</gresources>\n";
    return out.str();
}

// ── index.theme ───────────────────────────────────────────────────────────────
// For system-wide installation under /usr/share/icons/<theme_name>/

static std::string build_index_theme(
        const std::string& theme_name,
        const std::string& theme_comment,
        const std::set<std::string>& categories)
{
    // Collect all directory entries
    std::vector<std::string> dirs;
    dirs.push_back("scalable/actions");  // always list scalable first
    for (const auto& cat : categories) {
        if (cat != "actions")
            dirs.push_back("scalable/" + cat);
    }
    static constexpr int k_sizes[] = { 16, 24, 32, 48, 64, 128, 256 };
    for (int sz : k_sizes) {
        std::string s = std::to_string(sz);
        for (const auto& cat : categories)
            dirs.push_back(s + "x" + s + "/" + cat);
    }

    std::ostringstream out;
    out << "[Icon Theme]\n";
    out << "Name=" << theme_name << "\n";
    out << "Comment=" << theme_comment << "\n";
    out << "Inherits=hicolor\n";
    out << "Directories=";
    for (int i = 0; i < (int)dirs.size(); ++i) {
        if (i > 0) out << ",";
        out << dirs[i];
    }
    out << "\n\n";

    // Scalable sections
    for (const auto& cat : categories) {
        out << "[scalable/" << cat << "]\n";
        out << "Size=48\n";
        out << "MinSize=8\n";
        out << "MaxSize=512\n";
        out << "Type=Scalable\n";
        out << "Context=" << cat << "\n\n";
    }

    // Fixed-size sections
    for (int sz : k_sizes) {
        std::string s = std::to_string(sz);
        for (const auto& cat : categories) {
            out << "[" << s << "x" << s << "/" << cat << "]\n";
            out << "Size=" << sz << "\n";
            out << "Type=Fixed\n";
            out << "Context=" << cat << "\n\n";
        }
    }

    return out.str();
}

